        private static readonly IReadOnlyList<BeadColor> Empty = Array.Empty<BeadColor>();

        public static IReadOnlyList<BeadColor> Get(BeadBrand brand) => brand switch
        {
            BeadBrand.Mard => Mard,
            BeadBrand.ArtkalS => ArtkalS,
            BeadBrand.Perler => Perler,
            BeadBrand.Hama => Hama,
            BeadBrand.Nabbi => Nabbi,
