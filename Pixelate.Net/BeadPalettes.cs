        };

        // 交错 RGB 字节缓存：palette[0].R, palette[0].G, palette[0].B, palette[1].R, ...
        // 索引 0 对应 None（空数组），1..5 对应各品牌。
        private static readonly byte[][] _rgbCache = new byte[6][];

        /// <summary>
        /// 返回交错排列的 RGB 字节（R,G,B,R,G,B,...），供 ImagePixelator.NearestPaletteIndex 使用。
        /// 结果按品牌缓存。None 返回空数组。
        /// </summary>
        public static byte[] GetRgbBytes(BeadBrand brand)
        {
            int idx = (int)brand;
            byte[]? cached = _rgbCache[idx];
            if (cached is not null) return cached;

            var colors = Get(brand);
            byte[] bytes = new byte[colors.Count * 3];
            for (int i = 0; i < colors.Count; i++)
            {
                bytes[i * 3] = colors[i].R;
                bytes[i * 3 + 1] = colors[i].G;
                bytes[i * 3 + 2] = colors[i].B;
            }
            _rgbCache[idx] = bytes;
            return bytes;
        }

        /// <summary>
        /// 在品牌色卡中找加权距离最近的色索引（BT.601）。
        /// 供构建 5 位量化 bin 查找表使用。
        /// </summary>
        public static int NearestIndex(byte r, byte g, byte b, byte[] paletteRgb)
        {
            int palCount = paletteRgb.Length / 3;
            int best = 0;
            double bestDist = double.MaxValue;
            for (int i = 0; i < palCount; i++)
            {
                double dr = r - paletteRgb[i * 3];
                double dg = g - paletteRgb[i * 3 + 1];
                double db = b - paletteRgb[i * 3 + 2];
                double d = Wr * dr * dr + Wg * dg * dg + Wb * db * db;
                if (d < bestDist) { bestDist = d; best = i; }
            }
            return best;
        }
